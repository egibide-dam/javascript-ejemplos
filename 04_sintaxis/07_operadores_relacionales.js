// comparaciones
1 < 10; // = true
1 > 10; // = false
2 <= 2; // = true
2 >= 2; // = true

// Solo comprueba si los valores son iguales, no los tipos
"5" == 5; // = true

// Comprobar valor y tipo:
"5" === 5; // = false

alert('Z' > 'A'); // true
alert('Globo' > 'Gle'); // true
alert('Bee' > 'Be'); // true
alert('a' > 'A'); // true

alert('2' > 1); // true, string '2' se convierte a número 2
alert('01' == 1); // true, string '01' se convierte a número 1

alert(true == 1); // true
alert(false == 0); // true
